    public int changeSize() {
        try {
            ArrayList newList = new ArrayList();
            for (int i = 0; i < 5; i++) {
                if (chkboxList.get(i).isSelected()) {
                    for (int k = Integer.parseInt(arrivalList.get(i).getText()); k < Integer.parseInt(serviceList.get(i).getText()) + Integer.parseInt(arrivalList.get(i).getText()); k++) {
                        newList.add(k);
                    }
                }
            }
            return newList.size();
        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Invalid user input");
        }
        return 20;
    }

