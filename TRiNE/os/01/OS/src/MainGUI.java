    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // Step mode toggle clicked
        if (jToggleButton1.isSelected()) {                                      // If step mode turned on                                    
            jToggleButton1.setText("Off");
            jButton3.setEnabled(true);
            beginSimulator();
        } else {                                                                // If step mode turned off
            setIconsEnable(true);
            jButton3.setEnabled(false);
            jButton2.setEnabled(true);
            jToggleButton1.setText("On");
            processor.unitStepOn = false;
            processor.stepRunning = false;
            processor.setRestart("Switching simulation mode");
            jButton2.setText("Restart");
        }


    }//GEN-LAST:event_jToggleButton1ActionPerformed

