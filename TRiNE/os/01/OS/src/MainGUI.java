    public void beginSimulator() {                                              // Make ready all things and start processor
        setIconsEnable(false);                                                  // Disables all inputs

        if (jToggleButton1.isSelected()) {
            jButton2.setText("Start");
            jButton2.setEnabled(false);
            jButton1.setEnabled(false);
            jButton4.setEnabled(false);
        } else {
            jButton1.setEnabled(true);
            jButton2.setEnabled(true);
            jButton4.setEnabled(true);
            jButton2.setText("Restart");
        }

        for (JProgressBar pb : pbarList) {                                      // Reset progress bars
            pb.setString("Not Available");
        }

        if (graph != null) {                                                    // If restarting (graph is there when restarting)
            this.remove(graph);
            processor.setRestart("User requested.");
            for (JProgressBar jp : pbarList) {
                jp.setValue(0);
            }
        }

        jButton1.setText("Freeze");
        processor = new Processor(this);                                                // Make new processor instance
        graph = new Draw(new Dimension(welcomeLabel.getBounds().width, welcomeLabel.getBounds().height), processor.microStepsPerStep);
        graph.setBounds(welcomeLabel.getBounds());
        // Add graph to the frame
        add(graph);
        welcomeLabel.setVisible(false);                                         // Hide welcome label

        processor.unitStepOn = jToggleButton1.isSelected();                     // Set unit step mode in processor

        processor.start();                                                      // Start processor instance

    }
