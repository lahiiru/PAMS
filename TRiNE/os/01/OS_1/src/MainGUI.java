    public void beginSimulator() {
        this.jButton2.setText("Restart");

        if (graphic != null) {
            this.remove(graphic);
            p.setRestart();
            for (JProgressBar jp : pbarList) {
                jp.setValue(0);

            }

        }
        graphic = new Draw(new Dimension(welcomeLabel.getBounds().width, welcomeLabel.getBounds().height));
        graphic.setBounds(welcomeLabel.getBounds());
        System.out.println(graphic.getWidth()+" "+graphic.getWidth()/(changeSize()*20.0));
        graphic.cpulseWidth=graphic.buffer.getWidth()/(changeSize()*20);
        // Add to the frame
        add(graphic);
        welcomeLabel.setVisible(false);

        p = new Processor(this);
        p.unitStep = jToggleButton1.isSelected();
        p.start();

    }

