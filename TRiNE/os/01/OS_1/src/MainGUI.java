     //   ArrayList arrivalTimeList = new ArrayList();
     //   ArrayList serviceTimeList = new ArrayList();
        ArrayList newList = new ArrayList();

        if (jCheckBox1.isSelected()) {
            for (int k = Integer.parseInt(jTextField1.getText()); k < Integer.parseInt(jTextField6.getText())+Integer.parseInt(jTextField1.getText()); k++) {
                newList.add(k);
            }
        }
        if (jCheckBox2.isSelected()) {

            for (int k = Integer.parseInt(jTextField2.getText()); k <Integer.parseInt(jTextField7.getText())+Integer.parseInt(jTextField2.getText()); k++) {
                newList.add(k);
            }
        }
        if (jCheckBox3.isSelected()) {
            for (int k = Integer.parseInt(jTextField3.getText()); k <Integer.parseInt(jTextField8.getText())+Integer.parseInt(jTextField3.getText()); k++) {
                newList.add(k);
            }
        }
        if (jCheckBox4.isSelected()) {
            for (int k = Integer.parseInt(jTextField4.getText()); k < Integer.parseInt(jTextField9.getText())+Integer.parseInt(jTextField4.getText()); k++) {
                newList.add(k);
            }
        }
        if (jCheckBox5.isSelected()) {
            for (int k = Integer.parseInt(jTextField5.getText()); k < Integer.parseInt(jTextField10.getText())+Integer.parseInt(jTextField5.getText()); k++) {
                newList.add(k);
            }
        }
        System.out.println(newList.size());
        int t = 0;
        int n = 0;
        int value = 1;
        boolean s = true;
        
       
        while (newList.isEmpty()==false) {
            
            t++;
            s=true;
            while (s == true && newList.isEmpty()==false) {
               
                if (Integer.parseInt(newList.get(0).toString()) == t-1) {
                    
                    n++;
                    value++;
                    newList.remove(0);
                    
                } else {
                    if (n == 0) {
                        value++;
                    }
                    s = false;

                }

            }
          
        }
        System.out.println(value);
       
       return value;
